public class Tile {
    private color color;
    private int index;


    public Tile(color color, int index) {
        this.color = color;
        this.index = index;
    }

    public color getColor() {
        return color;
    }

    public void setColor(color color) {
        this.color = color;
    }


    public int getIndex() { return  index; }

    public void setIndex(int index) { this.index = index; }
}




